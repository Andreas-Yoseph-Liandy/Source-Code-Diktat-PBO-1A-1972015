package com.andreas.JavaSwingDanJDBC.entity;

public class Departement {
    private int id;
    private String name;

    public Departement() {

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
}
